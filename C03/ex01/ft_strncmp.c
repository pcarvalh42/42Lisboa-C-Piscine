#include <unistd.h>
#include <stdio.h>

int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n < 1)
		return (0);
	while (*s1++ == *s2++ && *(s1 - 1) && *(s2 - 1) && --n > 0)
		continue;
	return (*(s1 - 1) - *(s2 - 1));
}
