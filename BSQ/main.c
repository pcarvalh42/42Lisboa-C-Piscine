#include "ft_bsq.h"

int		main(int argc, char *argv[])
{
	int		i;

	i = 1;
	if (argc == 1)
		ft_stdin();
	while (i < argc)
	{
		ft_file_input(argv[i]);
		if (argc != 2)
			write(1, "\n", 1);
		i++;
	}
	return (0);
}
